# ── 工具函数 ──────────────────────────────────────────

def gan_index(gan: str) -> int:
    return TIAN_GAN.index(gan)


def zhi_index(zhi: str) -> int:
    return DI_ZHI.index(zhi)


def ganzhi_to_index(gan: str, zhi: str) -> int:
    """六十甲子序号 (0-59)"""
    g = gan_index(gan)
    z = zhi_index(zhi)
    for i in range(60):
        if i % 10 == g and i % 12 == z:
            return i
    return 0


def index_to_ganzhi(idx: int) -> Tuple[str, str]:
    idx = idx % 60
    return TIAN_GAN[idx % 10], DI_ZHI[idx % 12]


def get_nayin(gan: str, zhi: str) -> str:
    return NAYIN_TABLE[ganzhi_to_index(gan, zhi)]


def hour_to_shichen_zhi(hour: int) -> str:
    if hour >= 23 or hour < 1:
        return '子'
    return DI_ZHI[(hour + 1) // 2]


def hour_to_shichen_index(hour: int) -> int:
    if hour >= 23 or hour < 1:
        return 0
    return (hour + 1) // 2


def get_wuxing_relation(wx_a: str, wx_b: str) -> str:
    """返回 a 对 b 的关系: 同/生/被生/克/被克"""
    if wx_a == wx_b:
        return '同'
    if WUXING_SHENG.get(wx_a) == wx_b:
        return '生'
    if WUXING_SHENG.get(wx_b) == wx_a:
        return '被生'
    if WUXING_KE.get(wx_a) == wx_b:
        return '克'
    if WUXING_KE.get(wx_b) == wx_a:
        return '被克'
    return '无'


def solar_to_lunar(year: int, month: int, day: int) -> Dict:
    """阳历转农历"""
    try:
        from lunardate import LunarDate
        ld = LunarDate.fromSolarDate(year, month, day)
        return {
            'year': ld.year, 'month': ld.month, 'day': ld.day,
            'is_leap': ld.isLeapMonth,
            'month_cn': ('闰' if ld.isLeapMonth else '') + LUNAR_MONTH_CN[ld.month],
            'day_cn': LUNAR_DAY_CN[ld.day] if ld.day <= 30 else str(ld.day),
        }
    except Exception:
        return {'year': year, 'month': month, 'day': day, 'is_leap': False,
                'month_cn': str(month), 'day_cn': str(day)}


def lunar_to_solar(year: int, month: int, day: int, is_leap: bool = False) -> Optional[dt.date]:
    """农历转阳历"""
    try:
        from lunardate import LunarDate
        ld = LunarDate(year, month, day, isLeapMonth=is_leap)
        return ld.toSolarDate()
    except Exception:
        return None


def get_year_ganzhi(year: int) -> Tuple[str, str]:
    """年份干支 (以立春为界需外部处理)"""
    g = (year - 4) % 10
    z = (year - 4) % 12
    return TIAN_GAN[g], DI_ZHI[z]
